CREATE TABLE SEGMERCADO(
    ID NUMBER(5),
    DESCRICAO VARCHAR(100)
);

CREATE TABLE CLIENTE(
    ID NUMBER(5),
    RAZAO_SOCIAL VARCHAR(100),
    CNPJ VARCHAR(20),
    SEGMERCADO_ID NUMBER(5),
    DATA_INCLUSAO DATE,
    FAURAMENTO_PREVISTO NUMBER(10,2),
    CATEGORIA VARCHAR(20)
);

--  CHAVES PRIMARIAS
ALTER TABLE SEGMERCADO ADD CONSTRAINT PK_ID_SEGMERCADO
PRIMARY KEY (ID);

ALTER TABLE CLIENTE ADD CONSTRAINT PK_ID_CLIENTE
PRIMARY KEY(ID);

--  CHAVES ESTRANGEIRAS
ALTER TABLE CLIENTE ADD CONSTRAINT FK_CLIENTE_SEGMERCADO
FOREIGN KEY (SEGMERCADO_ID)
REFERENCES SEGMERCADO(ID);