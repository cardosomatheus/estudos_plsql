    (P_ID IN CLIENTE.ID%TYPE,
     P_FATURAMENTO_PREVISTO IN CLIENTE.FATURAMENTO_PREVISTO%TYPE);
     
     -- EXCLUIR O CLIENTE INFORMADO
     PROCEDURE EXCLUIR_CLIENTE
    (P_ID IN CLIENTE.ID%TYPE);
     -- CONSTANTES 
