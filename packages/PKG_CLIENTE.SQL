-- PACKAGE
CREATE OR REPLACE NONEDITIONABLE PACKAGE PKG_CLIENTE
IS
    PROCEDURE INCLUIR_CLIENTE 
    -- INCLUSÃO DE CLIENTE COM TODOS OS 5 PARAMETROS
   (P_ID IN CLIENTE.ID%TYPE,
    P_RAZAO_SOCIAL IN CLIENTE.RAZAO_SOCIAL%TYPE,
    P_CNPJ CLIENTE.CNPJ%TYPE ,
    P_SEGMERCADO_ID CLIENTE.SEGMERCADO_ID%TYPE,
    P_FATURAMENTO_PREVISTO CLIENTE.FATURAMENTO_PREVISTO%TYPE);
---------------------------------------------------------------------------------------------------
    PROCEDURE INCLUIR_CLIENTE 
    -- INCLUSÃO DE CLIENTE COM APENAS 3 PARAMETROS
   (P_ID IN CLIENTE.ID%TYPE,
    P_RAZAO_SOCIAL IN CLIENTE.RAZAO_SOCIAL%TYPE,
    P_SEGMERCADO_ID CLIENTE.SEGMERCADO_ID%TYPE);

    
